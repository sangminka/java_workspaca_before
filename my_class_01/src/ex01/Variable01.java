package ex01;

public class Variable01 {
	
	public static void main(String[] args) {
		
		int age;
		int count;
		
		age = 23;
		count = 100;
		
		System.out.println(age);
		System.out.println(count);
		
		age = 24;
		System.out.println(age);
		
		int year = 2023;
		
		System.out.println(year);
		System.out.println("-----------------------");
		
		System.out.println(300);
		System.out.println("수고링~");
		
	}//end of main

}//enf of class
